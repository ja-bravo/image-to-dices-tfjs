// Creates combinations take any two from array (like Py itertools.combinations)
// 'ABCD' -> 'ABACAD,BCBD,CD'
const combineAndOverlay = (tensorArray: tf.Tensor[]) => {
  const result = [];
  const startSize = tensorArray.length;
  for (let i = 0; i < startSize - 1; i++) {
    for (let j = i + 1; j < startSize; j++) {
      const overlay = tf.tidy(() => {
        // tf.less returns an array of true/false per pixel (Is pixel of I less than pixel of J)
        // tf.where returns the pixel of I or J depending on the result of tf.less
        // This basically combines the 2 images because they're black and white (1 black, 0 white)
        return tf.where(tf.less(tensorArray[i], tensorArray[j]), tensorArray[i], tensorArray[j]);
      });
      result.push(overlay);
    }
  }

  return [...tensorArray, ...result];
};

// This generates 9 versions of each dice.
// Each version is shifted by 1 pixel.
// This is called pad and crop augmentation
const generateShiftedImages = (tensor: tf.Tensor) => {
  const mutations: tf.Tensor[] = [];
  // Add 1px white padding to height and width
  const padded = tensor.pad(
    [
      [1, 1],
      [1, 1],
    ],
    1,
  );

  const cutSize = tensor.shape;
  for (let h = 0; h < 3; h++) {
    for (let w = 0; w < 3; w++) {
      const mutation = padded.slice([h, w], cutSize);
      mutations.push(mutation);
    }
  }

  padded.dispose();
  return mutations;
};

// This converts every dice into 200+ variations.
const augmentateDiceData = (diceTensor: tf.Tensor) => {
  const mutations = generateShiftedImages(diceTensor);
  const overlaidAndCombined = combineAndOverlay(mutations);
  const overlaidAndCombinedx2 = combineAndOverlay(overlaidAndCombined);

  // Remove duplicates and stack into a 3D tensor
  const groupedData = tf.stack(overlaidAndCombinedx2);
  const { values } = tf.unique(groupedData);
  tf.dispose([groupedData, overlaidAndCombined, overlaidAndCombinedx2, mutations]);
  return values;
};

const finalAugmentedData: any = {};
for (let i = 0; i < dices.length; i++) {
  const imgTensor = tf.tensor(dices[i]);
  const results = augmentateDiceData(imgTensor);
  console.log('Unique Results:', i, results.shape);
  finalAugmentedData[i] = results.arraySync();
  tf.dispose([imgTensor, results]);
}

fs.writeFileSync(`${__dirname}/dice-data.json`, JSON.stringify(finalAugmentedData));
